import { useNavigate } from 'react-router';
import { BASE_URL } from '../config/config';
import './ProfileDropdown.css';
import { useTranslation } from 'react-i18next';

export default function ProfileDropdown(props) {
    const {profileDropRef, handleProfPopupOpen, setIsPopup} = props;
    const {t} = useTranslation();

    const navigate = useNavigate();

    const handleEditProfile = (e) => {
        handleProfPopupOpen(e);
        profileDropRef.current?.classList.remove('profile-dropdown-active');
        setIsPopup(true);
    }

    const handleLogout = () => {
        fetch(`${BASE_URL}/logout`, {
            credentials: 'include',
            method: 'POST'
        }).then(res => res.json())
            .then(data => {
                navigate(data.redirect);
            })
    }

    return (
        <div className="profile-dropdown" ref={profileDropRef}>
            <span onClick={handleEditProfile}>
                <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" className="bi bi-pencil-square" viewBox="0 0 16 16">
                    <path d="M15.502 1.94a.5.5 0 0 1 0 .706L14.459 3.69l-2-2L13.502.646a.5.5 0 0 1 .707 0l1.293 1.293zm-1.75 2.456-2-2L4.939 9.21a.5.5 0 0 0-.121.196l-.805 2.414a.25.25 0 0 0 .316.316l2.414-.805a.5.5 0 0 0 .196-.12l6.813-6.814z" />
                    <path fillRule="evenodd" d="M1 13.5A1.5 1.5 0 0 0 2.5 15h11a1.5 1.5 0 0 0 1.5-1.5v-6a.5.5 0 0 0-1 0v6a.5.5 0 0 1-.5.5h-11a.5.5 0 0 1-.5-.5v-11a.5.5 0 0 1 .5-.5H9a.5.5 0 0 0 0-1H2.5A1.5 1.5 0 0 0 1 2.5z" />
                </svg>
                <p>{t('edit profile')}</p>
            </span>
            <span onClick={handleLogout}>
                <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" className="bi bi-box-arrow-right" viewBox="0 0 16 16">
                    <path fillRule="evenodd" d="M10 12.5a.5.5 0 0 1-.5.5h-8a.5.5 0 0 1-.5-.5v-9a.5.5 0 0 1 .5-.5h8a.5.5 0 0 1 .5.5v2a.5.5 0 0 0 1 0v-2A1.5 1.5 0 0 0 9.5 2h-8A1.5 1.5 0 0 0 0 3.5v9A1.5 1.5 0 0 0 1.5 14h8a1.5 1.5 0 0 0 1.5-1.5v-2a.5.5 0 0 0-1 0z" />
                    <path fillRule="evenodd" d="M15.854 8.354a.5.5 0 0 0 0-.708l-3-3a.5.5 0 0 0-.708.708L14.293 7.5H5.5a.5.5 0 0 0 0 1h8.793l-2.147 2.146a.5.5 0 0 0 .708.708z" />
                </svg>
                <p>{t('logout')}</p>
            </span>
        </div>
    );
}